                ),
              ),
            ),
          ),
        ],
      ),
    );
  }

  List<Widget> _buildTabContent(
      SleepState sleep, WaterState water, MedicationState meds) {
    switch (_activeTab) {
      case 0: // All
        return [
          _buildSleepCard(sleep),
          const SizedBox(height: 12),
          _buildWaterCard(water),
          const SizedBox(height: 12),
          _buildMedAdherenceCard(meds),
        ];
      case 1: // Sleep
        return [
          _buildSleepCard(sleep),
          const SizedBox(height: 12),
          _SleepStatsCard(sleep: sleep),
          const SizedBox(height: 12),
          _SleepHistoryCard(sleep: sleep),
        ];
      case 2: // Water
        return [
          _buildWaterCard(water),
          const SizedBox(height: 12),
          _WaterStatsCard(water: water),
        ];
      case 3: // Medications
        return [
          _buildMedAdherenceCard(meds),
          const SizedBox(height: 12),
          _MedListCard(meds: meds),
        ];
      default:
        return [];
    }
  }

  Widget _buildSleepCard(SleepState sleep) {
    final data = sleep.weeklyHours;
    if (data.isEmpty || data.every((h) => h == 0)) {
      return _EmptyDataCard(
        emoji: '🌙',
        title: 'Sleep Data',
        message: 'No sleep logs yet. Log your first sleep session!',
      );
    }
    return _SleepBarsCard(data: data, days: SleepState.weekLabels)
        .animate()
        .fadeIn(duration: 300.ms);
  }

  Widget _buildWaterCard(WaterState water) {
    // Build a 7-day mock using today's total (real history requires backend)
    if (water.totalMl == 0) {
      return _EmptyDataCard(
        emoji: '💧',
        title: 'Hydration Data',
        message: 'No water logged today. Start tracking your hydration!',
      );
    }
    return _WaterBarCard(todayMl: water.totalMl)
        .animate()
        .fadeIn(duration: 300.ms);
  }

  Widget _buildMedAdherenceCard(MedicationState meds) {
    if (meds.medications.isEmpty) {
      return _EmptyDataCard(
        emoji: '💊',
        title: 'Medication Data',
        message: 'No medications added yet.',
      );
    }
    final taken = meds.medications.where((m) => m.isTaken).length;
    final total = meds.medications.length;
    final pct = total > 0 ? taken / total : 0.0;
    return _AdherenceCard(taken: taken, total: total, pct: pct)
        .animate()
        .fadeIn(delay: 160.ms, duration: 300.ms);
  }
}

