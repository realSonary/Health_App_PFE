// ─── Water Bar Card ───────────────────────────────────────────────────────────

class _WaterBarCard extends StatelessWidget {
  final int todayMl;
  const _WaterBarCard({required this.todayMl});

  @override
  Widget build(BuildContext context) {
    final goalMl = AppConstants.dailyWaterGoalMl;
    final pct = (todayMl / goalMl).clamp(0.0, 1.0);

    return Container(
      decoration: cardDecoration(),
      padding: const EdgeInsets.all(16),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text('Hydration Today', style: AppTextStyles.cardTitle),
          const SizedBox(height: 2),
          Text(
            '${(todayMl / 1000).toStringAsFixed(2)} L of ${(goalMl / 1000).toStringAsFixed(1)} L goal',
            style: AppTextStyles.caption,
          ),
          const SizedBox(height: 16),
          // Big gauge
          TweenAnimationBuilder<double>(
            tween: Tween(begin: 0, end: pct),
            duration: const Duration(milliseconds: 800),
            curve: Curves.easeOutCubic,
            builder: (_, val, __) => Column(
              children: [
                ClipRRect(
                  borderRadius: BorderRadius.circular(999),
                  child: LinearProgressIndicator(
                    value: val,
                    minHeight: 18,
                    backgroundColor: AppColors.neutral100,
                    valueColor: AlwaysStoppedAnimation<Color>(
                      pct >= 1.0
                          ? AppColors.sage600
                          : const Color(0xFF2196F3),
                    ),
                  ),
                ),
                const SizedBox(height: 8),
                Row(
                  mainAxisAlignment: MainAxisAlignment.spaceBetween,
                  children: [
                    Text('${todayMl} ml consumed',
                        style: AppTextStyles.caption),
                    Text('${(pct * 100).round()}%',
                        style: AppTextStyles.caption.copyWith(
                          fontWeight: FontWeight.w700,
                          color: AppColors.plum700,
                        )),
                  ],
                ),
              ],
            ),
          ),
        ],
      ),
    );
  }
}

