class _AnalyticsScreenState extends ConsumerState<AnalyticsScreen> {
  bool _isWeekly = true;
  int _activeTab = 0; // 0=All, 1=Sleep, 2=Water, 3=Medications

  static const _tabs = ['All', 'Sleep', 'Water', 'Medications'];

  @override
  void initState() {
    super.initState();
    Future.microtask(() {
      ref.read(sleepProvider.notifier).loadRecent();
      ref.read(waterProvider.notifier).loadToday();
      ref.read(medicationProvider.notifier).load();
    });
  }

  // ── Share report as text summary ─────────────────────────────────────────
  void _shareReport() {
    final sleep = ref.read(sleepProvider);
    final water = ref.read(waterProvider);
    final meds = ref.read(medicationProvider);
