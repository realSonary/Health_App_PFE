// ─── Sleep Bars Card ──────────────────────────────────────────────────────────

class _SleepBarsCard extends StatelessWidget {
  final List<double> data;
  final List<String> days;

  const _SleepBarsCard({required this.data, required this.days});

  @override
  Widget build(BuildContext context) {
    final maxH = data.isEmpty ? 1.0 : data.reduce(math.max).clamp(1.0, 24.0);
    final avg = data.isEmpty
        ? 0.0
        : data.reduce((a, b) => a + b) / data.length;

    return Container(
      decoration: cardDecoration(),
      padding: const EdgeInsets.all(16),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text('Sleep Pattern', style: AppTextStyles.cardTitle),
          const SizedBox(height: 2),
          Text(
            'Avg ${avg.toStringAsFixed(1)}h · Recommended: 8h',
            style: AppTextStyles.caption,
          ),
          const SizedBox(height: 14),
          SizedBox(
            height: 100,
            child: Row(
              crossAxisAlignment: CrossAxisAlignment.end,
              mainAxisAlignment: MainAxisAlignment.spaceAround,
              children: List.generate(data.length, (i) {
                final h = data[i];
                final barH = ((h / maxH) * 80).clamp(2.0, 80.0);
                final isToday = i == data.length - 1;
                final label = i < days.length ? days[i] : '';
                final isGood = h >= 7;

                return Column(
                  mainAxisAlignment: MainAxisAlignment.end,
                  children: [
                    if (h > 0)
                      Text(
                        '${h.toStringAsFixed(1)}',
                        style: TextStyle(
                          fontSize: 8,
                          fontWeight: FontWeight.w700,
                          color: isToday ? AppColors.plum900 : AppColors.neutral400,
                        ),
                      ),
                    const SizedBox(height: 2),
                    TweenAnimationBuilder<double>(
                      tween: Tween(begin: 0, end: barH),
                      duration: Duration(milliseconds: 400 + i * 50),
                      curve: Curves.easeOutCubic,
                      builder: (_, val, __) => Container(
                        width: 28,
                        height: val,
                        decoration: BoxDecoration(
                          color: isGood
                              ? (isToday ? AppColors.plum900 : AppColors.plum500)
                              : AppColors.rose400,
                          borderRadius: const BorderRadius.vertical(
                              top: Radius.circular(5)),
                        ),
                      ),
                    ),
                    const SizedBox(height: 5),
                    Text(
                      label,
                      style: TextStyle(
                        fontSize: 9,
                        fontWeight: isToday ? FontWeight.w700 : FontWeight.w500,
                        color: isToday
                            ? AppColors.plum900
                            : AppColors.neutral400,
                      ),
                    ),
                  ],
                );
              }),
            ),
          ),
        ],
      ),
    );
  }
}

// ─── Sleep History Card ───────────────────────────────────────────────────────

class _SleepHistoryCard extends StatelessWidget {
  final SleepState sleep;
  const _SleepHistoryCard({required this.sleep});

  @override
  Widget build(BuildContext context) {
    final logs = sleep.historyLogs;
    if (logs.isEmpty) {
      return Container(
        decoration: cardDecoration(),
        padding: const EdgeInsets.all(18),
        child: Column(children: [
          const Text('📅', style: TextStyle(fontSize: 30)),
          const SizedBox(height: 8),
          Text('No sleep history yet', style: AppTextStyles.bodySemiBold),
          const SizedBox(height: 4),
          Text('Past sleep sessions will appear here',
              style: AppTextStyles.caption, textAlign: TextAlign.center),
        ]),
      );
    }

    return Container(
      decoration: cardDecoration(),
      padding: const EdgeInsets.all(16),
      child: Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
        Row(children: [
          const Text('📅', style: TextStyle(fontSize: 18)),
          const SizedBox(width: 8),
          Text('Sleep History', style: AppTextStyles.cardTitle),
          const Spacer(),
          Text('${logs.length} sessions', style: AppTextStyles.caption),
        ]),
        const SizedBox(height: 12),
        const Divider(height: 1),
        ...logs.take(14).map((log) {
          final startStr = log['sleep_start'] as String?;
          final hours = SleepState.safeDuration(log);
          final quality = (log['quality'] as num?)?.toInt() ?? 0;
          DateTime? start;
          try { if (startStr != null) start = DateTime.parse(startStr); } catch (_) {}
          final dateLabel = start != null
              ? DateFormat('EEE, MMM d').format(start) : '—';

          final Color barColor = hours >= 7
              ? AppColors.sage500
              : hours >= 5
                  ? const Color(0xFFF59E0B)
                  : AppColors.rose400;

          return Padding(
            padding: const EdgeInsets.symmetric(vertical: 9),
            child: Row(children: [
              SizedBox(
                width: 88,
                child: Text(dateLabel,
                    style: AppTextStyles.caption.copyWith(
                        fontWeight: FontWeight.w600,
                        color: AppColors.textSecondary)),
              ),
              Expanded(
                child: ClipRRect(
                  borderRadius: BorderRadius.circular(999),
                  child: LinearProgressIndicator(
                    value: (hours / 9).clamp(0.0, 1.0),
                    backgroundColor: AppColors.border,
                    valueColor: AlwaysStoppedAnimation(barColor),
                    minHeight: 8,
                  ),
                ),
              ),
              const SizedBox(width: 8),
              Text('${hours.toStringAsFixed(1)}h',
                  style: TextStyle(
                      fontSize: 11,
                      fontWeight: FontWeight.w700,
                      color: barColor)),
              const SizedBox(width: 6),
              Row(children: List.generate(5, (i) => Icon(
                i < quality ? Icons.star_rounded : Icons.star_outline_rounded,
                color: Colors.amber, size: 10))),
            ]),
          );
        }),
      ]),
    );
  }
}

