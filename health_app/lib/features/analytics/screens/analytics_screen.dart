📊 SensiaHealth Weekly Report
━━━━━━━━━━━━━━━━━━━━━━━
🌙 Sleep: ${sleep.lastNightHours.toStringAsFixed(1)}h last night
   Avg this week: ${sleep.averageHours.toStringAsFixed(1)}h/night
