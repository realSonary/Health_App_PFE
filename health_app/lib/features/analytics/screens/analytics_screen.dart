        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
      ),
    );
    Clipboard.setData(ClipboardData(text: summary));
  }

  // ── Export PDF (calls backend or shows pending state) ──────────────────
  void _exportPdf() {
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: const Text('📄 Preparing your PDF report…'),
        backgroundColor: AppColors.plum700,
        behavior: SnackBarBehavior.floating,
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
        action: SnackBarAction(
          label: 'OK',
          textColor: Colors.white,
          onPressed: () {},
        ),
      ),
    );
    // TODO: POST /reports/export?format=pdf and open the returned URL
  }

  void _exportCsv() {
    final sleep = ref.read(sleepProvider);
    final water = ref.read(waterProvider);
    final csv = [
      'Type,Value,Unit,Date',
      'Sleep,${sleep.lastNightHours.toStringAsFixed(1)},hours,${DateTime.now().toIso8601String().split("T").first}',
